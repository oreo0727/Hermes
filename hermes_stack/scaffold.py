def repo_root(root_dir: str | Path | None = None) -> Path:
    if root_dir is not None:
        return Path(root_dir).resolve()
    return Path(__file__).resolve().parents[2]


def hermes_state_dir(root_dir: str | Path | None = None) -> Path:
    return repo_root(root_dir) / "state" / "hermes"


def profiles_dir(root_dir: str | Path | None = None) -> Path:
    return hermes_state_dir(root_dir) / "profiles"


def _policy_path(root_dir: Path) -> Path:
    return root_dir / "config" / "policies" / "workspace-roots.json"


def _workspace_roots(root_dir: Path) -> dict[str, Path]:
    payload = json.loads(_policy_path(root_dir).read_text(encoding="utf-8"))
    roots: dict[str, Path] = {}
    for raw in payload.get("approvedRoots", []):
        path = Path(raw).resolve()
        roots[path.name] = path
    return roots


def _read_env_file(path: Path) -> dict[str, str]:
    values: dict[str, str] = {}
    if not path.exists():
        return values
    for line in path.read_text(encoding="utf-8").splitlines():
        stripped = line.strip()
        if not stripped or stripped.startswith("#") or "=" not in stripped:
            continue
        key, value = stripped.split("=", 1)
        values[key.strip()] = value.strip()
    return values


def _write_env_file(path: Path, mapping: OrderedDict[str, str]) -> None:
    lines = ["# Generated by scripts/bootstrap-hermes.sh", ""]
    for key, value in mapping.items():
        lines.append(f"{key}={value}")
    lines.append("")
    path.write_text("\n".join(lines), encoding="utf-8")


def _write_if_missing(path: Path, content: str) -> None:
    if not path.exists():
        path.write_text(content.rstrip() + "\n", encoding="utf-8")


def _write_text(path: Path, content: str) -> None:
    path.write_text(content.rstrip() + "\n", encoding="utf-8")


def _docker_available() -> bool:
    try:
        completed = subprocess.run(
            ["docker", "version", "--format", "{{.Server.Version}}"],
            check=False,
            capture_output=True,
            text=True,
        )
    except FileNotFoundError:
        return False
    return completed.returncode == 0 and bool(completed.stdout.strip())


def _load_source_env(root_dir: Path) -> dict[str, dict[str, str]]:
    seed_root: Path | None = None
    seed_file = root_dir / ".hermes-seed-source"
    if seed_file.exists():
        configured = seed_file.read_text(encoding="utf-8").strip()
        if configured:
            seed_root = Path(configured).expanduser()
    env_override = os.environ.get("HERMES_SEED_SOURCE", "").strip()
    if env_override:
        seed_root = Path(env_override).expanduser()
    local_dir = (seed_root / "config" / "env" / "local") if seed_root else (root_dir / ".missing-seed-source")
    return {
        "base": _read_env_file(local_dir / "base.env"),
        "gateway": _read_env_file(local_dir / "gateway.env"),
        "discord_main": _read_env_file(local_dir / "discord-main.env"),
        "discord_creative": _read_env_file(local_dir / "discord-creative.env"),
    }


def _existing_api_key(profile_home: Path) -> str:
    current = _read_env_file(profile_home / ".env")
    value = current.get("API_SERVER_KEY", "").strip()
    return value or secrets.token_hex(24)


def _profile_home(root_dir: Path, spec: ProfileSpec) -> Path:
    return profiles_dir(root_dir) / spec.key


def _workspace_root(root_dir: Path, spec: ProfileSpec) -> Path:
    return _workspace_roots(root_dir)[spec.workspace_name]


def _terminal_backend() -> str:
    return "docker" if _docker_available() else "local"


def _terminal_cwd(root_dir: Path, spec: ProfileSpec) -> Path:
    if spec.terminal_scope == "repo":
        return root_dir
    return _workspace_root(root_dir, spec)


def _build_profile_env(root_dir: Path, spec: ProfileSpec, source_env: dict[str, dict[str, str]]) -> OrderedDict[str, str]:
    profile_home = _profile_home(root_dir, spec)
    api_key = _existing_api_key(profile_home)
    current_env = _read_env_file(profile_home / ".env")
    base_env = source_env["base"]
    discord_env = source_env["discord_main"]
    mapping: OrderedDict[str, str] = OrderedDict()
    mapping["API_SERVER_ENABLED"] = "true"
    mapping["API_SERVER_HOST"] = "127.0.0.1"
    mapping["API_SERVER_PORT"] = str(spec.api_port)
    mapping["API_SERVER_KEY"] = api_key
    mapping["OPENAI_API_KEY"] = base_env.get("OPENAI_API_KEY", "")
    mapping["OPENAI_ORG_ID"] = base_env.get("OPENAI_ORG_ID", "")
    mapping["OPENAI_PROJECT_ID"] = base_env.get("OPENAI_PROJECT_ID", "")
    if spec.gateway_enabled and discord_env.get("DISCORD_MAIN_ENABLED", "").lower() == "true":
        mapping["DISCORD_BOT_TOKEN"] = discord_env.get("DISCORD_MAIN_TOKEN", "")
        mapping["DISCORD_ALLOWED_USERS"] = discord_env.get("DISCORD_MAIN_ALLOWED_USERS", "")
        mapping["DISCORD_ALLOWED_ROLES"] = discord_env.get("DISCORD_MAIN_ALLOWED_ROLES", "")
    if spec.key == "operator":
        mapping["DISCORD_WAKE_WORDS"] = current_env.get("DISCORD_WAKE_WORDS", "") or "Sheldon"
    return mapping


