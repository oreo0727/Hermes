def list_cognitive_records(
    root_dir: str | Path | None = None,
    kind: str = "events",
    *,
    agent_slug: str = "",
    limit: int = 50,
) -> list[dict[str, Any]]:
    root = repo_root(root_dir)
    config = COGNITIVE_TABLES.get(kind)
    if config is None:
        raise ValueError(f"Unknown cognitive record kind: {kind}")
    normalized_slug = str(agent_slug or "").strip().lower()
    if not using_postgres(root):
        graph = _load_cognition_file(root)
        rows = [row for row in graph[kind].values() if isinstance(row, dict)]
        if normalized_slug:
            rows = [row for row in rows if str(row.get("agent_slug") or "").strip().lower() == normalized_slug]
        return rows[: max(1, limit)]

    ensure_postgres_ready(root)
    table = config["table"]
    order = config["order"]
    expr = _cognitive_json_expr(kind)
    with _connect(root) as conn:
        with conn.cursor() as cur:
            if normalized_slug and kind in {"events", "procedures", "reflections", "activations", "beliefs", "dream_jobs", "skill_evolutions", "agent_heartbeats", "agent_intentions"}:
                cur.execute(
                    f"""
                    SELECT {expr}
                    FROM {table}
                    WHERE agent_slug = %s
                    ORDER BY {order}
                    LIMIT %s
                    """,
                    (normalized_slug, max(1, limit)),
                )
            else:
                cur.execute(
                    f"""
                    SELECT {expr}
                    FROM {table}
                    ORDER BY {order}
                    LIMIT %s
                    """,
                    (max(1, limit),),
                )
            return [_load_json_value(row[0]) for row in cur.fetchall()]
