def _port_open(host: str, port: int) -> bool:
    with socket.socket(socket.AF_INET, socket.SOCK_STREAM) as sock:
        sock.settimeout(0.25)
        return sock.connect_ex((host, port)) == 0


def _pid_alive(pid: int) -> bool:
    if pid <= 0:
        return False
    try:
        os.kill(pid, 0)
    except OSError:
        return False
    return True


def _detect_hermes_binary(root: Path) -> str:
    candidates = (
        root / "state" / "hermes" / "venv" / "bin" / "hermes",
        Path.home() / ".local" / "bin" / "hermes",
    )
    for candidate in candidates:
        if candidate.exists():
            return str(candidate)
    try:
        completed = subprocess.run(["which", "hermes"], check=False, capture_output=True, text=True)
    except FileNotFoundError:
        return ""
    return completed.stdout.strip()


def _read_json_file(path: Path) -> Any:
    if not path.exists():
        return {}
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except (json.JSONDecodeError, OSError):
        return {}


def _config_scalar(path: Path, key: str, default: str = "") -> str:
    if not path.exists():
        return default
    prefix = f"{key}:"
    for line in path.read_text(encoding="utf-8").splitlines():
        stripped = line.strip()
        if stripped.startswith(prefix):
            return stripped.removeprefix(prefix).strip().strip("\"'")
    return default


def _load_gateway_runtime(profile_home: Path) -> dict[str, Any]:
    payload = _read_json_file(profile_home / "gateway_state.json")
    if not isinstance(payload, dict):
        payload = {}
    pid_payload = _read_json_file(profile_home / "gateway.pid")
    pid = 0
    if isinstance(pid_payload, dict):
        try:
            pid = int(pid_payload.get("pid", 0) or 0)
        except (TypeError, ValueError):
            pid = 0

    platform_rows: list[dict[str, Any]] = []
    raw_platforms = payload.get("platforms")
    if isinstance(raw_platforms, dict):
        for name, details in raw_platforms.items():
            if not isinstance(details, dict):
                continue
            platform_rows.append(
                {
                    "name": str(name),
                    "state": str(details.get("state", "unknown")),
                    "updated_at": str(details.get("updated_at", "")),
                }
            )
    platform_rows.sort(key=lambda row: row["name"])

    connected_platforms = [row["name"] for row in platform_rows if row["state"] == "connected"]
    gateway_state = str(payload.get("gateway_state", "stopped"))
    pid_live = _pid_alive(pid)
    if gateway_state == "running" and not pid_live:
        gateway_state = "stopped"
    return {
        "gateway_state": gateway_state,
        "live": gateway_state == "running" and pid_live,
        "active_agents": int(payload.get("active_agents", 0) or 0),
        "restart_requested": bool(payload.get("restart_requested", False)),
        "updated_at": str(payload.get("updated_at", "")),
        "connected_platforms": connected_platforms,
        "platforms": platform_rows,
        "pid": pid,
        "pid_live": pid_live,
    }


def _load_recent_sessions(profile_home: Path, *, limit: int = 5) -> list[dict[str, Any]]:
    payload = _read_json_file(profile_home / "sessions" / "sessions.json")
    if not isinstance(payload, dict):
        return []

    rows: list[dict[str, Any]] = []
    for session_key, session in payload.items():
        if not isinstance(session, dict):
            continue
        origin = session.get("origin")
        origin_payload = origin if isinstance(origin, dict) else {}
        rows.append(
            {
                "session_key": str(session.get("session_key", session_key)),
                "session_id": str(session.get("session_id", "")),
                "updated_at": str(session.get("updated_at", "")),
                "created_at": str(session.get("created_at", "")),
                "platform": str(session.get("platform", "")),
                "chat_type": str(session.get("chat_type", "")),
                "display_name": str(session.get("display_name", "")),
                "user_name": str(origin_payload.get("user_name") or ""),
                "chat_name": str(origin_payload.get("chat_name") or ""),
                "chat_id": str(origin_payload.get("chat_id") or ""),
                "thread_id": str(origin_payload.get("thread_id") or ""),
                "suspended": bool(session.get("suspended", False)),
            }
        )
    rows.sort(key=lambda row: row["updated_at"], reverse=True)
    return rows[:limit]


def _build_activity_snapshot(profile_rows: list[dict[str, Any]], projects: list[dict[str, Any]]) -> dict[str, Any]:
    operator_profile = next((row for row in profile_rows if row["key"] == "operator"), {})
    operator_runtime = operator_profile.get("runtime") if isinstance(operator_profile.get("runtime"), dict) else {}
    operator_sessions = operator_profile.get("recent_sessions") if isinstance(operator_profile.get("recent_sessions"), list) else []

    hot_count = sum(1 for project in projects if project.get("activity_state") == "hot")
    warm_count = sum(1 for project in projects if project.get("activity_state") == "warm")
    idle_count = sum(1 for project in projects if project.get("activity_state") == "idle")

    busy_input_mode = str(operator_profile.get("busy_input_mode", "unknown"))
    return {
        "operator": {
            "busy_input_mode": busy_input_mode,
            "interruptible": busy_input_mode == "interrupt",
            "runtime": operator_runtime,
            "recent_sessions": operator_sessions,
            "latest_session_at": operator_sessions[0]["updated_at"] if operator_sessions else "",
        },
        "projects": {
            "hot_count": hot_count,
            "warm_count": warm_count,
            "idle_count": idle_count,
            "recent": projects[:6],
        },
    }


