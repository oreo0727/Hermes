RUNTIME_ENV_NAME = "runtime.env"
DEFAULT_BACKEND = "file"
DEFAULT_DATABASE_URL = "postgresql:///hermes_control"
MIGRATION_META_KEY = "file_migration_v1"
MEMORY_MIGRATION_META_KEY = "agent_memory_seed_v1"


def repo_root(root_dir: str | Path | None = None) -> Path:
    if root_dir is not None:
        return Path(root_dir).resolve()
    return Path(__file__).resolve().parents[1]


def hermes_state_dir(root_dir: str | Path | None = None) -> Path:
    return repo_root(root_dir) / "state" / "hermes"


def runtime_env_path(root_dir: str | Path | None = None) -> Path:
    return hermes_state_dir(root_dir) / RUNTIME_ENV_NAME


def _read_env_file(path: Path) -> dict[str, str]:
    values: dict[str, str] = {}
    if not path.exists():
        return values
    for line in path.read_text(encoding="utf-8").splitlines():
        stripped = line.strip()
        if not stripped or stripped.startswith("#") or "=" not in stripped:
            continue
        key, value = stripped.split("=", 1)
        values[key.strip()] = value.strip()
    return values


def _write_env_file(path: Path, mapping: dict[str, str]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    lines = ["# Generated by Hermes state storage", ""]
    for key in sorted(mapping):
        lines.append(f"{key}={mapping[key]}")
    lines.append("")
    path.write_text("\n".join(lines), encoding="utf-8")


def runtime_settings(root_dir: str | Path | None = None) -> dict[str, str]:
    root = repo_root(root_dir)
    file_values = _read_env_file(runtime_env_path(root))
    backend = os.environ.get("HERMES_STATE_BACKEND", file_values.get("HERMES_STATE_BACKEND", DEFAULT_BACKEND)).strip().lower() or DEFAULT_BACKEND
    database_url = os.environ.get("HERMES_DATABASE_URL", file_values.get("HERMES_DATABASE_URL", "")).strip()
    return {
        "backend": backend,
        "database_url": database_url or (DEFAULT_DATABASE_URL if backend == "postgres" else ""),
    }


def state_backend(root_dir: str | Path | None = None) -> str:
    return runtime_settings(root_dir).get("backend", DEFAULT_BACKEND)


def database_url(root_dir: str | Path | None = None) -> str:
    return runtime_settings(root_dir).get("database_url", "")


def configure_runtime(
    root_dir: str | Path | None = None,
    *,
    backend: str,
    database_url_value: str = "",
) -> dict[str, str]:
    root = repo_root(root_dir)
    normalized_backend = (backend or DEFAULT_BACKEND).strip().lower() or DEFAULT_BACKEND
    payload = {
        "HERMES_STATE_BACKEND": normalized_backend,
    }
    if normalized_backend == "postgres":
        payload["HERMES_DATABASE_URL"] = database_url_value.strip() or DEFAULT_DATABASE_URL
    else:
        payload["HERMES_DATABASE_URL"] = ""
    _write_env_file(runtime_env_path(root), payload)
    return runtime_settings(root)


def using_postgres(root_dir: str | Path | None = None) -> bool:
    return state_backend(root_dir) == "postgres" and bool(database_url(root_dir))


def _ensure_psycopg_importable(root_dir: Path) -> None:
    try:
        import psycopg  # noqa: F401
        return
    except ImportError:
        pass

    site_packages = sorted((root_dir / "state" / "hermes" / "venv").glob("lib/python*/site-packages"))
    for path in site_packages:
        if str(path) not in sys.path:
            sys.path.insert(0, str(path))


def _load_json_value(value: object) -> dict[str, Any]:
    if isinstance(value, dict):
        return value
    if isinstance(value, str):
        try:
            loaded = json.loads(value)
        except json.JSONDecodeError:
            return {}
        return loaded if isinstance(loaded, dict) else {}
    return {}


@contextmanager
def _connect(root_dir: str | Path | None = None) -> Iterator[Any]:
    root = repo_root(root_dir)
    _ensure_psycopg_importable(root)
    import psycopg

    conn = psycopg.connect(database_url(root), autocommit=True)
    try:
        yield conn
    finally:
        conn.close()


